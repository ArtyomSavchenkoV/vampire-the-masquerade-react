import { Modifiers } from "./Modifiers";

/**
 * Модель активного эффекта (бафф/дебафф, состояние и пр.)
 */
export interface ActiveEffect {
  /** Уникальный идентификатор эффекта. */
  id: string;

  /** Человекочитаемое название эффекта. */
  name: string;

  /** Тип эффекта: бафф или дебафф. */
  type: "buff" | "debuff";

  /**
   * Модификаторы, влияющие на характеристики персонажа.
   * Поле необязательное: некоторые эффекты нужны только для сценарной логики
   * (например, «Персонаж под гипнозом» или «Замечен охраной») и не меняют статы.
   */
  modifiers?: Modifiers;

  /** Длительность эффекта: в раундах или сцене. */
  duration?: number | "scene";

  /** Источник эффекта. */
  source: "clan" | "merit" | "item" | "spell" | "environment";
}
