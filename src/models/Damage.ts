/**
 * Типы урона в V20.
 * Критически важны для расчёта поглощения (Fortitude) и финального исхода боя.
 */
export type DamageType =
  /** Тупой/дробящий урон: кулаки, дубинки, падения. Обычно поглощается легче всего. */
  | "bashing"
  /** Смертельный урон: ножи, пули, когти, огонь. Стандартный урон, пробивающий защиту. */
  | "lethal"
  /** Усугублённый/сверхсмертельный урон: солнечный свет, святое пламя, серебряные клинки, некоторые дисциплины.
   * Единственный тип, который может привести к окончательной смерти вампира (Final Death). */
  | "aggravated";
